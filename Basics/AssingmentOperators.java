package Basics;

public class AssingmentOperators {
    public static void main(String[] args) {
        int a = 10;
        a += 10;
        System.out.println(a);
        int b = 5;
        b -= 5;
        System.out.println(b);
        int c = 10;
        c *= 5;
        System.out.println(c);
        int d = 10;
        d /= 10;
        System.out.println(d);
    }
}
